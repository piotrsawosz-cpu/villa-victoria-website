// Global language state + event bus
(function() {
  const saved = (typeof localStorage !== 'undefined' && localStorage.getItem('vv-lang')) || 'en';
  window.__VV_LANG = saved;
  window.__VV_LISTENERS = new Set();
  window.setVVLang = (code) => {
    const lc = code.toLowerCase();
    if (!window.__VV_TRANSLATIONS[lc]) return;
    window.__VV_LANG = lc;
    try { localStorage.setItem('vv-lang', lc); } catch(e) {}
    document.documentElement.lang = lc;
    window.__VV_LISTENERS.forEach(fn => fn(lc));
  };
  window.t = (key) => {
    const dict = window.__VV_TRANSLATIONS[window.__VV_LANG] || window.__VV_TRANSLATIONS.en;
    return dict[key] ?? window.__VV_TRANSLATIONS.en[key] ?? key;
  };
  // React hook
  window.useVVLang = () => {
    const [lang, setLang] = React.useState(window.__VV_LANG);
    React.useEffect(() => {
      const fn = (lc) => setLang(lc);
      window.__VV_LISTENERS.add(fn);
      return () => window.__VV_LISTENERS.delete(fn);
    }, []);
    return lang;
  };
})();
